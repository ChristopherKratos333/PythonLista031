'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

valor1 = float(input("Digite 3 números no total. Primeiro número: "))

valor2 = float(input("Segundo número: "))

valor3 = float(input("Terceiro número: "))

media = (valor1 + valor2 + valor3) / 3

print("A média aritmética desses três números é:", media)